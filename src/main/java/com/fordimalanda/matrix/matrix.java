package com.fordimalanda.matrix;

public class Matrix {
    private int a1, a2, a3, a4;

    public Matrix(int a1, int a2, int a3, int a4) {
        this.a1 = a1;
        this.a2 = a2;
        this.a3 = a3;
        this.a4 = a4;
    }

    public int getA1() { 
        return a1; 
        }
    public int getA2() { 
        return a2; 
        }
    public int getA3() { 
        return a3;
        }
    public int getA4() {
         return a4;
        }
}